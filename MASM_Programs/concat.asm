.MODEL SMALL
.DATA
MSG1 DB "Computer$"
MSG2 DB "Science$"
.CODE

MOV AX, @DATA
MOV DS, AX

LEA SI, MSG1
LEA DI, MSG2

MOV AL, '$'
UP: CMP AL, [SI]
JZ NEXT
INC SI
JMP UP

NEXT: CMP AL, [DI]
JZ EXIT
MOV BL, [DI]
MOV [SI], BL
INC SI
INC DI
JMP NEXT

EXIT: MOV [SI], AL
LEA DX, MSG1
MOV AH, 09H
INT 21H

MOV AH,4CH
INT 21H
END

