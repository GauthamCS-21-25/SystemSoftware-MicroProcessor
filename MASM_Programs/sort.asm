.MODEL SMALL
.DATA
LIST DB 2H, 9H, 8H, 5H, 3H 
.CODE
MOV AX,@DATA
MOV DS,AX

MOV CL,04H
L1: MOV CH,05H
MOV SI,OFFSET LIST
L2: MOV AL,[SI]
INC SI
CMP AL,[SI]
JC SKIP
XCHG AL,[SI]
DEC SI
XCHG AL,[SI]
INC SI
SKIP: DEC CH
JNZ L2
DEC CL
JNZ L1

MOV CX,05H
MOV SI,OFFSET LIST
PRINT: MOV AL, [SI]
CMP AL, 0AH
JGE ALPHA
ADD AL, 30H
JMP DISPLAY
ALPHA: ADD AL, 37H
DISPLAY: MOV DL, AL;
MOV AH, 02H
INT 21H
INC SI
LOOP PRINT


MOV AH,4CH
INT 21H
END

